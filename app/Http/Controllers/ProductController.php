     */
    public function addProduct(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'name' => 'required',
        ]);

        if ($validator->fails()) {
            return redirect('/')
                        ->withErrors($validator)
                        ->withInput();
        }
        $product = new Product();
        $product->product_name = $request->input('name');
        if($product->save()) {
            $products = Product::all();
            return response()->json( [ 'products' => $products, 'status'=> 200, 'message' => 'Product added successfully' ] );
        }else{
            return response()->json( ['status'=> 500, 'message' => 'Product added successfully' ] );
        }

       
    }

